# how it work: while conditional:
# as long as the conditional is true the code will be executed

# example of using a while loop
x = 1
while x <= 10:
    print(x)
    x = x + 1
print('Done1')

# Repeating a string with a while loop
y = 1
while y <= 5:
    print('*' * y)
    y = y + 1
print('Done2')

