# print answer before doing the calculation first
print(10 * 5)

# addition
print(10 + 5)

# subraction
print(10 - 5)

# multiplication
print(10 * 5)

# division
print(10 / 7)  # gives float
print(10 // 7)  # rounds to nearest integer
print(10 % 7)  # gives remainder of the division

# power of
print(10 ** 5)

# increment a number
x = 10
x = x + 5
print(x)
# same but with augmented assignment operator(adds a value to existing variable and assign the new value back to the same variable
x += 5
print(x)
