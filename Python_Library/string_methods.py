# find and print the length of a string
string = ('hello world')
print(len(string))

# change string using a method
string2 = 'hello world'
print(string2.upper())

# find a sequence of characters in a string
string3 = 'hello world'
print(string3.find('world'))  # tell you the start of the character sequence

# replace the characters in the string
string4 = 'hello world'
print(string4.replace('world', 'future world'))

# find if a string contain a word or sequence of words
string5 = 'hello world'
print('world' in string5)  # gives boolean value
